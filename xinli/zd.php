  <ul> <p>是的，人是会长大的；可就在那长大的瞬间，仿佛又要失去些什么。</p>
　　<p>上幼儿园时，妈妈告诉我：“你要快点长大，好帮我分担一些。”</p>
　　<p>上小学时，妈妈告诉我：“你已经是大孩子了，要学会做一些事情，懂吗？”</p>
　　<p>上初中时，妈妈告诉我：“孩子，你已经长大了，如果再来一次，我一定不会再期盼这我怀里的宝贝随着长大而离开我的怀抱。”</p>
　　<p>长大？为什么要长大？我多么想永远都不要长大，永远做个孩子依偎在妈妈的怀抱中。</p>
　　<p>长大，就像有一首歌里唱的：“越长大越孤单。”是啊，小时候，周围一圈都是朋友、同学、亲人，可是慢慢的，朋友变少了，同学们变少了，亲人们也变少了。我开始不断的换学校、换地方、换住所。幼儿园的时候，整个幼儿园的人都是我的朋友，我有爸爸、妈妈、爷爷、奶奶，还有我的发小--虞。小学的时候，整个班都是我的朋友，我有爸爸、妈妈、爷爷，奶奶去世了，我还有发小。现在，我身边只有两个好朋友，她们是我的闺蜜，我有爸爸、妈妈、爷爷，我也没有发小了，虞去世了。</p>
　　<p>我知道，时间不等人，岁月也不饶人。也许十年后，我连爷爷都没有了；又或者五十年后，我连爸爸妈妈都没有了，在过个几十年，也许，我就也不存在于这个世界了。现在，无论多大委屈，我都可以依偎在妈妈的怀里讲诉，过个几十年，就可能变成我的孩子依偎在我的怀里讲诉了。妈妈告诉我：难过的时候，就抱紧自己大哭一场。爸爸告诉我:沮丧的时候，就去跟他聊聊天。闺蜜们告诉我：无论以后怎样。有什么事发生都没关系，有她们在。可也许多少年后，他们就只能在我心里了。亲人会永恒分开，友人会分别。</p>
　　<p>在梦里，我紧抱着时间哭诉着：“我求求你，你慢点走，我想让他们多留在我身边一会儿，不要那么快的离我而去。”可时间却不领情，挣脱出我的怀抱，溜走了。又一个梦：一个声音说，“你长大了，孩子！”那声音是那样的熟悉，可刚讲完，就逃走了。那是妈妈的声音。我竭力嘶吼着：“不，我还是个孩子啊！我还需要人保护，我还需要人安慰，我还需要你的拥抱啊！”梦醒，一大滴眼泪徘徊在眼角，慢慢地滑落脸颊，越过耳垂，渗透了我的心。</p>
　　<p>时间啊！你走慢点，让他们慢点变老，让他们多多陪陪我吧！让我慢点长大，慢点学会这处世之道。</p>  </ul>
   
