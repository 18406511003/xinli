      <span class="biaotifont">成为更好的人——访信息学院刘聪</span> <span style="float: right">2018-1-10</span><br></br>
    </div>  
<div class = "xxinwen">
 
<p>这一次采访，由于不知道如何下手如何找人，于是跑去了老乡群，一提起这件事，大家不约而同地提起了这位学长，于是我带着自己的疑问进行了这一次的采访。</p>
<p>我觉得一个人的信念是非常重要的，你的人生信条是什么，你就会成为什么样的人。所以我想了解的第一点就是“人生信条是什么”，学长告诉我，他所想所做都是为了——Be better！有一句话叫做“取法乎上，仅得其中；取法乎中，仅得其下；取法乎下，所得无几”，而学长所想的就是要做得更好，简单却也复杂，如果我们总能超越自己，总能成为更好的人，相信一切都会不一样。
</p>
<p>那么大化小，从人生转移到眼前，我问学长：“你如何评价自己的大学四年？”“大一太严肃，属于傻X学霸类；大二太较真，各种不服输；大三才像上大学，做着自己喜欢的事；大四放纵，做着以前不敢做的事！”学长如是回答。人生的各个阶段，都有自己命定去做的事，但唯一不改变的，就是我们都在成长。从单纯学习到认真挑战，再到真正地成为自己，然后努力去突破，每一个阶段都那么珍贵而又充满着各自的热情，带着自己独特的青春的色彩。而我们的一切已经不能说才开始了，所以要更认真地去做自己了，然后记着自己最初的梦想。“我最初的梦想很简单，就是做一个niubility的人，到现在还记得呀！”其实我们的初衷都很简单，繁复而无聊的生活渐渐打垮了我们而我们却不自知，我想说，无论我们最初的梦想是什么，而它于我们又有多难，我们都应当时不时回头看看一路走来的自己，不迷乱于人世的繁华。
</p>
<p>“大三的时候最迷茫，想不通自己应该去做什么。每天呆在实验室很辛苦，但是却感觉自己没有任何收获。”其实呢，“谁的青春不迷茫，其实我们都一样”。学长说，他那个时候用的方法就是加入一个新的环境，认识新的人，突然一天，你就会发现自己不再迷茫了。
</p>
<p>优秀的人一定有一些常人所没有的习惯，学长说，他自认为自己一直在坚持的，就是自我反思。他说，在这个过程中，我们可以发现自己的缺点，虽然不一定能改掉，但在其中可以让我们更容易找到真正的自己。
</p>
<p>学长说自己就是一个没有洁癖的处女座，平时喜欢自己动手DIY一些电路，弹弹尤克里里；他还说自己并不优秀，每个人都值得成为更好的人。
</p>
<p>这位学长就是来自信息学院的刘聪，曾获IT杯二等奖、挑战杯三等将、国家奖学金等荣誉。
</p>

