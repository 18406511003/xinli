    <p> 12月8日晚7点30分，大学生创造学会第18届OM头脑风暴大赛决赛在二号教学楼x2222拉开帷幕。本次大赛由大学生创造学会承办。大赛邀请了指导老师蒋洪波老师、法律协会会长和计算机与网络协会副会长担任嘉宾兼大赛评委。从初赛到决赛，在数百名参赛选手中脱颖而出的的10组选手各展才能，精彩答辩，为了大家展示了自己对于创造的热忱和努力。</p>
 
<p>李娜在读高中时，学校倡导大学化教育开设校园学生会和各个社团，当时他担任班上团支书，同时任职学生会生活部，五个社团部长，并自己独立创立羽毛球社，组织校级比赛，奠定创业应具备的一些基本素质，如人际交往能力、组织能力、管理能力等。</p>
<p>本次决赛主题为：灯。各小组尽施才能，都带了精美的作品。决赛现场评分规则由“微信投票结果”（占总比10%）、“创意”（占总比30%）、“答辩”（占总比30%）、“废物利用”（占总比30％）四个环节组成。在答辩环节，选手们分别围绕“创意来源”、“制作过程”、“材料展示”、“创意理念”、“现场效果展示”以及“评委问答”六个环节进行了三分钟答辩展示，在展示他们优秀的创造能力和创新思维的同时，也体现了当代大学生对社会、环境，人文和情感的关注与思考。</p>
<p>答辩完成后，各小组将自己的作品展示在第一排长桌上，所有参赛选手相互欣赏彼此的作品，交流心得，在这种互相学习的氛围中形创新思维能力的横向拓展。</p>
<p> 经过了两个小时的激烈角逐，各个奖项尘埃落定，比赛共决出一等奖一名，二等奖两名，三等奖三名。冠军由来自茅以升学院的贾春霖，顾金池，张嘉航，黄垠钦同学获得，该小组制作的浪漫情调日式灯以出色的外观，别具一格的创意，实用性以及环保的制作流程获得全体评委和其它参赛选手一致好评。
</p>
<p>最后大赛评委对此次比赛做出了精辟的点评，对大赛也做出了充分肯定。得奖的参赛选手在现场带着自己的精致作品合影留念，并于创学成员合影留念。本次OM头脑风暴大赛完美收官。</p>


